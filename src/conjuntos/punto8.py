# Creamos un conjunto llamado 'numeros'.
# Los conjuntos automáticamente eliminan los elementos duplicados.
# En este caso, el número 1 aparece dos veces, pero el conjunto solo lo guardará una vez.
numeros = {3, 1, 4, 1, 5, 9}

# Imprimimos el conjunto.
# El resultado mostrará los números únicos, pero el orden puede cambiar cada vez que se ejecute el código,
# ya que los conjuntos no guardan un orden específico.
print(numeros)
