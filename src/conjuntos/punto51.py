# Tres conjuntos de números
conjunto_a = {1, 2}
conjunto_b = {1, 2, 3}  # Tiene un número más
conjunto_c = {1, 2}     # Es igual al primero

# Pregunto si el primer conjunto es más pequeño que el segundo
print(conjunto_a < conjunto_b)  # True - A es más pequeño que B
print(conjunto_a < conjunto_c)  # False - Son iguales, no es más pequeño