# Tres conjuntos con números
conjunto_a = {1, 2, 3}
conjunto_b = {3, 1, 2}  # Tienen los mismos números pero en otro orden
conjunto_c = {1, 2, 3, 4}  # Tiene un número extra

# Pregunto si los conjuntos son iguales (mismos elementos)
print(conjunto_a == conjunto_b)  # True - Tienen los mismos números
print(conjunto_a == conjunto_c)  # False - No son iguales