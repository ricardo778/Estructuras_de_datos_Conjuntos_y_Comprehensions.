# Conjuntos de asistentes del grupo A y grupo B.
grupo_a = {"Ana", "Carlos", "Elena", "David"}
grupo_b = {"Carlos", "Elena", "Fernando"}

# Encontramos los asistentes que están en ambos grupos (intersección).
comunes = grupo_a.intersection(grupo_b)

# Imprimimos el conjunto con los nombres comunes.
print(comunes)
