# Aquí creamos un conjunto.
# Los conjuntos solo guardan elementos únicos. Por eso, "ana" y "carlos" aparecen una sola vez.
usuarios = set(["ana", "carlos", "ana", "elena", "carlos"])

# La función 'print' muestra el conjunto final sin los nombres repetidos.
print(usuarios)  # Imprime: {'ana', 'elena', 'carlos'}