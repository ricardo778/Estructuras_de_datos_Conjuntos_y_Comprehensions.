# Creamos un conjunto de ciudades.
ciudades = {"Madrid", "Barcelona", "Valencia", "Sevilla"}

# Usamos un bucle 'for' para pasar por cada ciudad en el conjunto.
# Los conjuntos no tienen un orden específico, así que la salida puede variar.
for ciudad in ciudades:
    # Imprimimos un mensaje por cada ciudad que visitamos.
    print(f"Visitando {ciudad}")