# Conjunto de números pares.
numeros_pares = {2, 4, 6, 8}

# Conjunto de números del 1 al 9.
numeros = {1, 2, 3, 4, 5, 6, 7, 8, 9}

# Verifica si 'numeros_pares' es un subconjunto de 'numeros'.
print(numeros_pares.issubset(numeros))

# También se puede usar el operador '<=' para la misma verificación.
print(numeros_pares <= numeros)
