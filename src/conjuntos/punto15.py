# Creamos un conjunto llamado 'numeros' con 5 elementos.
numeros = {1, 2, 3, 4, 5}

# Usamos el método '.clear()' para eliminar todos los elementos del conjunto.
# El conjunto quedará vacío.
numeros.clear()

# Imprimimos el conjunto para ver el resultado.
# Se mostrará un conjunto vacío.
print(numeros)
