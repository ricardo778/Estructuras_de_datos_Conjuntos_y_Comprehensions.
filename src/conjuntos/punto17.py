# Creamos un conjunto llamado 'dias' con los días de la semana de lunes a viernes.
dias = {"lunes", "martes", "miércoles", "jueves", "viernes"}

# Usamos el operador 'in' para verificar si "sábado" está en el conjunto.
# El resultado es False, ya que "sábado" no es parte de este conjunto.
print("sábado" in dias)

# Verificamos si "lunes" está en el conjunto.
# El resultado es True, ya que "lunes" sí es parte del conjunto.
print("lunes" in dias)