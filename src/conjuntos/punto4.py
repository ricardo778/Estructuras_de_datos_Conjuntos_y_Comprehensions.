# Creamos un conjunto llamado 'frutas' con algunos nombres de frutas.
frutas = {"manzana", "naranja", "plátano"}

# Verificamos si "manzana" está en el conjunto. La función 'print' lo confirma.
print("manzana" in frutas)  # Imprime: True

# Ahora, verificamos si "uva" está en el conjunto. 'print' nos dice que no está.
print("uva" in frutas)      # Imprime: False
