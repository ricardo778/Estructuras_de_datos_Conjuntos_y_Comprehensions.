# Creamos un conjunto llamado 'tecnologias' con tres elementos.
tecnologias = {"Python", "JavaScript", "SQL"}

# Usamos el método '.add()' para agregar un nuevo elemento al conjunto.
# Si el elemento ya existiera, '.add()' no haría nada y no daría error.
tecnologias.add("Java")

# Imprimimos el conjunto actualizado.
# El resultado mostrará los cuatro elementos, pero el orden puede variar.
print(tecnologias)