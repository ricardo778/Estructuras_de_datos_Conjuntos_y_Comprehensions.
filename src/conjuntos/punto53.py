# Tres conjuntos de números
conjunto_a = {1, 2, 3, 4}
conjunto_b = {2, 3}        # Tiene menos números
conjunto_c = {1, 2, 3, 4}  # Es igual al primero

# Pregunto si el primer conjunto es más grande que el segundo
print(conjunto_a > conjunto_b)  # True - A es más grande que B
print(conjunto_a > conjunto_c)  # False - Son iguales, no es más grande